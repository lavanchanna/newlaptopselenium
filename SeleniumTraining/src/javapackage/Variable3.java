package javapackage;

public class Variable3 {
	
	public static void main(String[] args) {
		
		
		int a[] = {1,2,3,4,5};
		String b[] = {"Hello","How","Are","you"};
		System.out.println(a[1]);
		System.out.println(b[2]);
		int i=2;
		System.out.println(i++);
		System.out.println(i);
		System.out.println(i--);
		System.out.println(i);
		System.out.println(++i);
		System.out.println(--i);
		int d=11;
		System.out.println(d++ + ++d);
		System.out.println(d);
		
	}

}
