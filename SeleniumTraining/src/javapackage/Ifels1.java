package javapackage;

public class Ifels1 {
	public static void main(String[] args) {
		
		//if else ladder statment
		int q=400;
		int w=300;
		if (q==w)
		{
			System.out.println("q equal to w");
		}
		else if (q>w)
		{
			System.out.println("q is greate than w");
		}
		else
		{
			System.out.println("w is greater than q");
		}
		
		
	}

}
